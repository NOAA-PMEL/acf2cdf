package acf2cdf;

import java.util.Date;

/**
 *
 * @author derek
 */
public class ACFTimeGap {
    int dataPoint;
    Date start;
    Date end;

    public ACFTimeGap(int dataPoint, Date start, Date end) {
        this.dataPoint = dataPoint;
        this.start = start;
        this.end = end;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }

    public Date getStart() {
        return start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    public int getDataPoint() {
        return dataPoint;
    }

    public void setDataPoint(int dataPoint) {
        this.dataPoint = dataPoint;
    }


}
